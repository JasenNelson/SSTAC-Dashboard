import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { runAudit } from './audit-library-provenance.mjs';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const REPO_ROOT = path.resolve(__dirname, '..', '..');

export function generateReport(options = {}) {
  const findings = options.findings || runAudit();
  const reportPath = options.outputPath || path.join(REPO_ROOT, 'docs', 'MATRIX_OPTIONS_PROVENANCE_AUDIT_REPORT_2026_07_04.md');

  const counts = { high: 0, medium: 0, low: 0 };
  const checkCounts = {};

  for (const f of findings) {
    const severity = f.severity || 'low';
    counts[severity] = (counts[severity] || 0) + 1;

    const checkType = f.check || 'UNKNOWN';
    checkCounts[checkType] = (checkCounts[checkType] || 0) + 1;
  }

  const localDateStr = new Date().toLocaleDateString('en-CA');
  let md = `# Substance Library Provenance Audit Report\n\n`;
  md += `Generated by \`generate-provenance-audit-report.mjs\` (run on ${localDateStr})\n\n`;

  md += `## 1. Summary of Findings\n\n`;
  md += `| Severity | Count |\n`;
  md += `| :--- | :--- |\n`;
  md += `| **High** (Value Mismatch / Missing Approved Row / Type Error) | ${counts.high} |\n`;
  md += `| **Medium** (Missing Citation / Stale Notes) | ${counts.medium} |\n`;
  md += `| **Low** / Info | ${counts.low} |\n`;
  md += `| **Total Findings** | **${findings.length}** |\n\n`;

  md += `### Breakdown by Check Type\n\n`;
  md += `| Check Type | Count |\n`;
  md += `| :--- | :--- |\n`;
  for (const [checkType, count] of Object.entries(checkCounts).sort((a, b) => b[1] - a[1])) {
    md += `| \`${checkType}\` | ${count} |\n`;
  }
  md += `\n`;

  md += `## 2. Details by Substance\n\n`;

  // Group findings by substance key
  const grouped = {};
  for (const f of findings) {
    const key = f.key || 'system';
    if (!grouped[key]) {
      grouped[key] = [];
    }
    grouped[key].push(f);
  }

  const sortedSubstances = Object.keys(grouped).sort((a, b) => {
    if (a === 'system') return -1;
    if (b === 'system') return 1;
    return a.localeCompare(b);
  });

  for (const subKey of sortedSubstances) {
    const subFindings = grouped[subKey];
    md += `### Substance: \`${subKey}\` (${subFindings.length} findings)\n\n`;
    md += `| Check | Severity | Detail |\n`;
    md += `| :--- | :--- | :--- |\n`;
    for (const f of subFindings) {
      const detailText = f.detail || '';
      const parts = [];
      if (f.library_value !== undefined) {
        parts.push(`Library: \`${f.library_value}\``);
      }
      if (f.catalog_values !== undefined) {
        parts.push(`Catalog Options: \`[${f.catalog_values.join(', ')}]\``);
      }
      if (f.missing_pvid !== undefined) {
        parts.push(`Missing citation: \`${f.missing_pvid}\``);
      }
      const extraInfo = parts.length > 0 ? ` (${parts.join('; ')})` : '';
      const severity = (f.severity || 'low').toUpperCase();
      md += `| \`${f.check}\` | **${severity}** | ${detailText}${extraInfo} |\n`;
    }
    md += `\n`;
  }

  fs.writeFileSync(reportPath, md, 'utf8');
  console.log(`Successfully generated provenance audit report containing ${findings.length} findings at: ${reportPath}`);
  return reportPath;
}

// CLI Execution Handler
if (process.argv[1] && fileURLToPath(import.meta.url) === fs.realpathSync(process.argv[1])) {
  generateReport();
  process.exit(0);
}
